#include "3-calc.h"
/**
 * op_add - adds stuff
 * @a: one int
 * @b: the other int
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts
 * @a: ...
 * @b: ...
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies
 * @a: ...
 * @b: ...
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides
 * @a: ...
 * @b: ...
 * Return: int
 */
int op_div(int a, int b)
{
	return ( a / b);
}
/**
 * op_mod - modulo
 * @a: ...
 * @b: ...
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
